func EncodeField(name string) string {
	value := []rune(name)
	for i, r := range value {
		if v, ok := mapping[r]; ok {
			value[i] = v
		}
	}
	return string(value)
}

func DecodeField(name string) string {
	value := []rune(name)
	for i, r := range value {
		if v, ok := reverseMapping[r]; ok {
			value[i] = v
		}
	}
	return string(value)
}
